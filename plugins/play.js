let limit = 30
let yts = require('yt-search')
const { servers, yta, ytv } = require('../lib/y2mate')

let handler = async (m, { conn, command, text, isPrems, isOwner }) => {

  await m.reply('Searching...')
  if (!text) throw 'Mau cari lagu apa?'
  let results = await yts(text)
  let vid = results.all.find(video => video.seconds < 3600)
  if (!vid) throw 'Lagu tidak ditemukan!'
  let { dl_link, thumb, title, filesize, filesizeF} = await (/2$/.test(command) ? ytv : yta)(vid.url, 'id4')
  let isLimit = (isPrems || isOwner ? 99 : limit) * 1024 < filesize
  conn.sendFile(m.chat, thumb, 'thumbnail.jpg', `
*Title:* ${title}
*Filesize:* ${filesizeF}
*Source:* ${vid.url}
*${isLimit ? 'Pakai ': ''}Link:* ${dl_link}

Lagu ${isLimit ? 'tidak akan dikirim,, karna ukuran file terlalu besar anda bisa download via Link': 'sedang dikirim,, harap tunggu sekitar 1 menit.'}
`.trim(), m)
  if (!isLimit) conn.sendFile(m.chat, dl_link, title + '.mp' + (3 + /2$/.test(command)), `
*Title:* ${title}
*Filesize:* ${filesizeF}
*Source:* ${vid.url}

`.trim(), m)
}
handler.help = ['play', 'play2', 'lagu', 'musik', 'ytsong'].map(v => v + ' <lagu>')
handler.tags = ['music']
handler.command = /^(play|play2|lagu|musik|ytsong)$/i

handler.exp = 0
handler.limit = true

module.exports = handler

